<h2>الأسئلة الشائعة</h2>

<h3>ما هو الحد الأدنى للاستثمار؟</h3>
<p>الحد الأدنى يختلف حسب المشروع، لكنه عادة يبدأ من 1,000 ريال سعودي، مما يجعل الاستثمار متاحاً للجميع.</p>

<h3>متى أستلم العوائد؟</h3>
<p>تُوزّع العوائد بشكل دوري (شهري، ربع سنوي، أو سنوي) حسب نوع المشروع، وتحوّل مباشرة إلى محفظتك.</p>

<h3>هل يمكنني بيع أسهمي في أي وقت؟</h3>
<p>نعم، بعد انتهاء فترة القفل (إن وجدت)، يمكنك بيع أسهمك في السوق الثانوي.</p>

<h3>هل المنصة مرخصة؟</h3>
<p>نعم، منصة سهمي مرخصة من الجهات التنظيمية المختصة وخاضعة لرقابة صارمة.</p>

<h3>هل يوجد رسوم خفية؟</h3>
<p>لا، جميع الرسوم معلنة بشفافية كاملة. لا توجد أي رسوم مخفية.</p>

<h3>ماذا يحدث إذا فشل المشروع؟</h3>
<p>في حالة فشل المشروع (نادر جداً)، هناك آليات حماية قانونية، وقد يتم تصفية الأصول وتوزيع العوائد حسب الحصص.</p>

